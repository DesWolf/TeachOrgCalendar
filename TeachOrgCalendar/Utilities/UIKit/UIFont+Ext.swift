import UIKit

extension UIFont {
    class var title: UIFont {
      return UIFont.systemFont(ofSize: 35.0, weight: .semibold)
    }
    
    class var button: UIFont {
      return UIFont.systemFont(ofSize: 15.0, weight: .regular)
    }
}


